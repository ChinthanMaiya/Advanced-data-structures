package tree;

public class RemovalHelper
{
	private Node node;
	private int c;
	private boolean isRoot;
	public Node getNode() 
	{
		return node;
	}
	public void setNode(Node node) 
	{
		this.node = node;
	}
	public int getC() 
	{
		return c;
	}
	public void setC(int c) 
	{
		this.c = c;
	}
	public boolean isRoot() 
	{
		return isRoot;
	}
	public void setIsRoot(boolean isRoot) 
	{
		this.isRoot = isRoot;
	}
	
}
